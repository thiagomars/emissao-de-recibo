package model.bean;

public class Ativar {
    
    private String status_ativar;
    private String data_ativar;
    private int id;

    public String getStatus_ativar() {
        return status_ativar;
    }

    public void setStatus_ativar(String status_ativar) {
        this.status_ativar = status_ativar;
    }

    public String getData_ativar() {
        return data_ativar;
    }

    public void setData_ativar(String data_ativar) {
        this.data_ativar = data_ativar;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
}
